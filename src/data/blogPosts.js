export const blogCategories = [...new Set(blogPosts.map((post) => post.category))];

export const blogSeoKeywords = [...new Set(blogPosts.flatMap((post) => post.keywords))];

export function getBlogPostBySlug(slug) {
  return blogPosts.find((post) => post.slug === slug);
}

export function getBlogPath(postOrSlug) {
  const slug = typeof postOrSlug === "string" ? postOrSlug : postOrSlug.slug;
  return `/blog/${slug}`;
}

export function getRelatedBlogPosts(post, limit = 3) {
  return post.relatedSlugs
    .map((slug) => getBlogPostBySlug(slug))
    .filter(Boolean)
    .slice(0, limit);
}
